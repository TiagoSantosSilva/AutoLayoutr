import UIKit

func getPinkColor() -> UIColor {
    let pinkColor = UIColor(red: 232/255, green: 68/255, blue: 133/255, alpha: 1)
    return pinkColor
}

func getFadedPinkColor() -> UIColor {
    let fadedPinkColor = UIColor(red: 249/255, green: 207/255, blue: 224/255, alpha: 1)
    return fadedPinkColor
}
